function result = add_core_data(core1,core2)
result=struct('primitives',0,'clbs',0,'slices',0,'lut_sites',0,'luts',0,'reg',0,'srl16s',0,'dist_ram',0,'bram',0,'mults',0,'hu_sets',0);
fields = fieldnames(result);
for i=1:length(fields)
    field = fields{i};
    result = setfield(result,field,getfield(core1,field)+getfield(core2,field));
end